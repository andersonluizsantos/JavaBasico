package api;

public class ExplorandoMath {
	public static void main(String[] args) {
		// Conjunto de Métodos Estáticos 
		System.out.println(Math.PI);
		//Potenciação
		System.out.println(Math.pow(2, 3));
		//valor abosoluto
		System.out.println(Math.abs(-10.5));
		//retorna o menor valor entre dois números
		System.out.println(Math.min(70, 7));
		//retorna o maior valor entre dois números
		System.out.println(Math.max(70, 7));
		//Arrendondar o número para cima
		System.out.println(Math.ceil(6.55)); // Prof. Legal
		//Arrendondar o número para baixo
		System.out.println(Math.floor(6.55)); // Prof. Otário
		//Gera um valor aleatório entre 0 e 1
		System.out.println(Math.random());
		// Math.
	}
}
